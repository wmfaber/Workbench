<h1>Handleiding</h1>
<p>Dit is de handleiding voor het gebruik maken van dit product.</p>
<ol>
	<li>
		<b>Upload</b>
		<p>Hier kunt u bestellingen (Excel bestand) uploaden naar de database. <br/>Dit doet u te klikken op <kbd>Bestand kiezen</kbd> en vervolgens selecteerd u het bestand. De bestanden van de diverse bedrijven dienen volgens vaste indelingen geupload te worden. Alles wat afwijkt wordt geweigerd om foute data te voorkomen.</p>
	</li>
	<li>
		<b>Bestellingen</b>
		<p>Hier ziet u een overzicht van de geuploade bestellingen. De bestellingen zijn te downloaden als .csv bestand.<br/>Elke bestelling is te bewerken. Klik op de bewerkknop(<i class="glyphicon glyphicon-edit"></i>) en u opent de bewerkpagina. U kunt downloaden door linksonderin op <kbd>download</kbd> te klikken.<br/>
		Zoeken naar bestellingen kan door rechts bovenin in de zoekbalk te typen. Bladeren tussen pagina's kan door rechts onderin op de paginanummers te klikken. Het aantal geladen regels is standaard 500. Wilt u meer resultaten ophalen, dan kunt u dat doen door links bovenin op de 'Aantal geladen regels' te klikken.</p>
	</li>
	<li>
		<b>Producten</b>
		<p>Hier kunt u voor ieder uniek product een synoniem en bijpassende envelop invoeren. Hiervoor klikt u op de bewerkknop (<i class="glyphicon glyphicon-edit"></i>)<br/>
		Het bladeren tussen pagina's en zoeken gaat het zelfde als bij de Bestellingen.</p>
	</li>
	<li>
		<b>Login</b>
		<p>Hier kunt u inloggen <!--ook kunt u hier een gebruiker toevoegen. Klik op <kbd>Registreren</kbd> onder het inlog formulier. vervolgen voert u uw email en 2keer uw wachtwoord in. 
		<br/>Nadat u heeft geregristreed ontvangt u een mail op het door u ingevoerde email adress. Open de link in uw mail en u kunt inlogen met uw email en wachtwoord.--></p>
	</li>
</ol>
